CREATE TABLE IF NOT EXISTS public.shelf_companies (
    id bigint generated by default as identity primary key,
    name text not null,
    age text not null,
    price numeric not null,
    vat_registered boolean not null default false,
    sic_code text not null,
    created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable public read access so Vercel can fetch the companies
ALTER TABLE public.shelf_companies ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Enable read access for all users" ON public.shelf_companies
    AS PERMISSIVE FOR SELECT
    TO public
    USING (true);

